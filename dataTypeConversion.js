
// dataTypeConversion

// 1. String to Number
let str = "100";
let num = Number(str);
console.log(num);
console.log(typeof(num));

// 2. Number to String
let num1 = 100;
let str1 = String(num1);
console.log(str1);
console.log(typeof(str1));

// 3. Boolean to Number
let bool = false;
let num2 = Number(bool);
console.log(num2);
console.log(typeof(num2));

